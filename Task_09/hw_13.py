shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100], ['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200], ['седло', 2700]]
thing = input('Название детали: ')
i = 0
sum = 0
for product in shop:
    if product[0] == thing:
        i = i + 1
        price = product[1]
        sum = sum + price
print('Кол-во деталей - ', i,'\n','Общая стоимость - ', sum)